;程序名：T3-3.ASM
;功能：压缩BDC码转换成ASCII码
DSEG	SEGMENT
BCD		DB	86H
ASCII	DB	2 DUP (0)
DSEG	ENDS
;
CSEG	SEGMENT
		ASSUME CS:CSEG, DS:DSEG
START:	MOV	AX, DSEG
		MOV 	DS, AX
		MOV	AL, BCD
		AND		AL, 0FH
		ADD		AL, 30H
		MOV	ASCII + 1,  AL
		MOV	AL, BCD
		MOV	CL, 4
		SHR		AL, CL
		ADD		AL, 30H
		MOV	ASCII, AL
		MOV	AH, 4CH
		INT		21H
CSEG	ENDS
		END		START