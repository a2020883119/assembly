;字校验
DSEG	SEGMENT
SUM		DW	?
DSEG	ENDS
CSEG	SEGMENT
		ASSUME	CS:CSEG 

START:	MOV	AX, 40H		;地址400开始1024个字节 1KB大小
		MOV	DS, AX		;
		
		MOV	SI, 0
		MOV	CX, 1024
		XOR		AX, AX

AGAIN:	ADD		AX, [SI]		;循环部分
		INC		SI
		INC		SI
		
		LOOP	AGAIN		
		
		ASSUME	DS:DSEG		;保存
		MOV	BX, DSEG
		MOV	DS, BX
		MOV	SUM, AX
		
		MOV	AH, 4CH
		INT		21H
CSEG	ENDS
		END	START