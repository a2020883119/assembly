;十六进制数字码到七段代码的转换
DSEG	SEGMENT
TAB		DB	1000000B, 1111001B, 0100100B, 0110000B	;七段代码表
		DB	0011001B, 0010010B, 0000010B, 1111000B
		DB	0000000B, 0010000B, 0001000B, 0000011B
		DB	1001100B, 0100001B, 0000110B, 0001110B
XCODE	DB	8
YCODE	DB	?
DSEG	ENDS
;
CSEG	SEGMENT
		ASSUME	CS:CSEG, DS:DSEG
START:	MOV	AX, DSEG
		MOV	DS, AX
		
		XOR		BX, BX			;清零
		MOV	BL, XCODE		;取值
		MOV	BH, 05H[BX]		;取表
		MOV	YCODE, BH		;存值
		
		MOV	AH, 4CH
		INT		21H
CSEG	ENDS
		END		START