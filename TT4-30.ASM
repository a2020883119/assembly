DSEG     SEGMENT
;         
ASCII    DB '................'
         DB '................'
         DB '. !"',"#%%&'(.)*+,-"
         DB './0123456789:;<.',
         DB '=>?@ABCDEFGHIJKL',
         DB 'MNOP.QRSTUVWXYZ['
         DB '\]^_`abcd.efghij'
         DB 'klmnopqrstuvwx.y'
         DB 'z{|}~.$.........'         
DSEG     ENDS

CSEG     SEGMENT
         ASSUME CS:CSEG, DS:DSEG
START:   
         MOV     AX, DSEG
         MOV     DS, AX

         MOV     DX, OFFSET ASCII
         CALL    DISPMESS

         MOV     AH, 4CH
         INT     21H
;功能：显示DX所指向的提示信息
;入口参数：dx字符串开始偏移地址
;出口参数：无
DISPMESS   PROC
           PUSH     AX
           MOV      AH, 9                      ;调用显示字符串中断
           INT      21H
           POP      AX
           RET
DISPMESS   ENDP

CSEG     ENDS
         END     START

